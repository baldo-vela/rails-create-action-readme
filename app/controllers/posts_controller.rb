class PostsController < ApplicationController
  def index
    @posts = Post.all
  end

  def show
    @post = Post.find(params[:id])
  end

  def new
    @post = Post.new
  end

  def create
    #'C' in the 'CRUD' life cycle â€” saves a new Post object and then redirects to the newly-created post's show page.
